maths = {'Algebra', 'Volume', 'Pythagoras', 'Scaling'}
art = {'Sketch', 'Scaling', 'Colour'}

# Union (Joining all item and remove duplicates)
print(maths.union(art))

# Intersect (Pointing out the same value)
print(maths.intersection(art))

# Difference (Find difference from first set)
print(maths.difference(art))